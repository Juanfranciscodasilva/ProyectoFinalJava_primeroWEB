    public boolean ValidarEmail(){
        Pattern p = Pattern.compile("^[_A-z0-9-]+(.[_A-z0-9-]+)*@[A-z0-9-]+(\\.[A-z0-9]+)*(\\.[A-z]{2,4})$");
         Matcher m = p.matcher(tEmail.getText());
         if(m.matches()){
             return true;
         }
         else{
             return false;
         } 
    }
    
    public boolean ValidarDireccion(){
        Pattern p = Pattern.compile("^[cC]/[ ]?[A-z]+([ ][A-z]+)*[ ][0-9]{1,3}[ ](([0-9]+|[pP][bB])[Aa|Bb|Cc|Dd|Ii|Ee|Ff])$");
         Matcher m = p.matcher(tDireccion.getText());
         if(m.matches()){
             return true;
         }
         else{
             return false;
         } 
    }
    
