    private void cbNombreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbNombreActionPerformed
        String nombre = cbNombre.getSelectedItem().toString();
        if(nombre.equals("-----Elegir campamento-----")){
            LimpiarCampos();
            Inscribir.setEnabled(false);
            InhabilitarPartePersona();
        }else{
             camp = new Campamento();
            camp = Main.ExtraerDatosCampamento(nombre);
            AutocompletarDatos();
            Inscribir.setEnabled(true);
            HabilitarPartePersona();
        }
    }//GEN-LAST:event_cbNombreActionPerformed

    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        if(Validar4PrimerosDatos()){
            LocalDate fecha = LocalDate.parse(tFechaNac.getText(), formatoFecha);
             per = new Persona(tNombre.getText(),tApellido1.getText(),tApellido2.getText(),fecha);
            try {
                Main.BuscarPersona(per,0);
            } catch (Exception ex) {
                System.out.println("Ha ocurrido un problema al buscar a la persona "+ ex.getMessage());
            }
        }else{
            eID.setVisible(false);
            tID.setVisible(false);
            ePE.setVisible(false);
            tTelefono.setEnabled(false);
            tEmail.setEnabled(false);
            tDireccion.setEnabled(false);
        }
        
    }//GEN-LAST:event_BuscarActionPerformed

