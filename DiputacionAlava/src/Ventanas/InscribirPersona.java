    private void InscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InscribirActionPerformed
        if(!tID.isVisible()){
            if(ValidarRestoDeDatos()){
                per.setDireccion(tDireccion.getText());
                per.setEmail(tEmail.getText());
                per.setTelefono(tTelefono.getText());
                try{
                    Main.InsertarNuevaPersona(per);
                    JOptionPane.showMessageDialog(null, "Persona añadida con éxito");
                    int codigo = Main.ObtenerCodigoPersona(per);
                    per.setId(codigo);
                }catch(Exception e){
                    System.out.println("Ha ocurrido un error al insertar a la persona "+e.getMessage());
                }
                InsertarPersonaEnCamp();
            }
        }else{
              InsertarPersonaEnCamp(); 
           }
        
        
        
    }//GEN-LAST:event_InscribirActionPerformed

